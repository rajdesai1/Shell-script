<?php

$var1 = 1;

# if else statment
if($var1 == 1){
    echo "It's 1.";
}
else{
    echo "It's not 1.";
}


# switch case statment
$var2 = 2;

switch($var2){
    case 1:
        echo "It's 1.";
        break;
    case 2:
        echo "It's 2.";
    default:
        echo "It's default.";
        break;
}

?>